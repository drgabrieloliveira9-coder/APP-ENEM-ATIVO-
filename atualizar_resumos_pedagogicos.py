import sqlite3
from pathlib import Path

DB_PATH = Path('data.db')

def atualizar_todos_resumos():
    """Atualiza todos os resumos com conte√∫do pedag√≥gico real e de qualidade"""
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()
    
    # Buscar todos os resumos
    cur.execute('''
        SELECT r.id, r.title, t.id as topic_id, a.name as area_name
        FROM resumos r
        JOIN topics t ON r.topic_id = t.id
        JOIN areas a ON t.area_id = a.id
        ORDER BY a.id, t.id
    ''')
    
    resumos = cur.fetchall()
    total = len(resumos)
    
    print(f'üìö Atualizando {total} resumos com conte√∫do pedag√≥gico real...\n')
    
    count = 0
    for resumo_id, title, topic_id, area_name in resumos:
        # Gerar conte√∫do pedag√≥gico de qualidade
        summary, bullets, quick_tip = gerar_conteudo_pedagogico(title, area_name)
        
        cur.execute('''
            UPDATE resumos 
            SET summary = ?, bullets = ?, quick_tip = ?
            WHERE id = ?
        ''', (summary, bullets, quick_tip, resumo_id))
        
        count += 1
        if count % 50 == 0:
            print(f'‚úÖ {count}/{total} resumos atualizados...')
    
    conn.commit()
    conn.close()
    
    print(f'\n‚úÖ CONCLU√çDO! {count} resumos atualizados com conte√∫do pedag√≥gico!')

